/**
 * @file result.js
 * @description Extension results page.
 *
 * This page opens automatically in a new tab when the user selects a
 * fragment of a web page and sends it for processing with an LLM service.
 *
 * DATA FLOW:
 * ---------------
 * Content to process is not passed directly as URL parameters (it could be
 * too long) but through `storage.local`:
 *
 *   1. background.js saves data under the `exec_<id>` key in storage.local.
 *   2. background.js opens this page with query: `result.html?id=<id>`.
 *   3. This script reads `id` from the query string, retrieves data from storage.local,
 *      and deletes it when finished (to avoid accumulating obsolete data).
 *
 * ARCHITECTURE FOR COMMUNICATION WITH BACKGROUND:
 * -------------------------------------------------
 * To receive text generated by the LLM in streaming, an extension Port is used
 * (runtime.connect), not simple messages. This allows:
 *   - Receiving multiple messages during a single "session" (one chunk per message).
 *   - Detecting unexpected disconnections and automatically retrying.
 *   - Not blocking the message channel with a response that takes minutes.
 *
 * RETRY SYSTEM:
 * ----------------------
 * Networks and LLM servers can fail mid-stream. The retry system
 * (`MAX_RETRIES = 3`) automatically launches new requests if a timeout is detected
 * (server stops responding) or an unexpected port disconnection occurs.
 * There are two timeouts:
 *   - 300,000 ms (5 min): For the first chunk. The model may take time to start.
 *   - 30,000 ms (30 s):   Between consecutive chunks. If stream stops unexpectedly.
 *
 * REAL-TIME RENDERING:
 * ----------------------------
 * As chunks arrive, they accumulate in `resultText` and are rendered as HTML
 * using the `parseMarkdown()` function, which converts basic Markdown syntax
 * (headers, bold, italics, code) to inline HTML.
 *
 * PROMPT VARIABLE SUBSTITUTION:
 * ----------------------------------------
 * The user-configured prompt can contain context variables that are
 * substituted at runtime with real data from the processed page:
 *   {DATE}    → Current date (toLocaleDateString)
 *   {HOUR}    → Current time (toLocaleTimeString)
 *   {URL}     → Full URL of the source page
 *   {DOMAIN}  → Only the domain of the source page
 *   {LANG}    → Language of the HTML document (<html lang="...">)
 *   {SYSLANG} → User's system/browser language (navigator.language)
 */

// Use `window.browser` (Firefox) with fallback to `window.chrome` for cross-compatibility
const api = window.browser || window.chrome;

document.addEventListener("DOMContentLoaded", async () => {

    // ---- DATA RETRIEVAL FROM STORAGE ----
    // Read execution ID from current URL query string.
    // URL example: result.html?id=1712345678901
    const urlParams = new URLSearchParams(window.location.search);
    const execId = urlParams.get('id');

    if (!execId) {
        // Error case: page opened directly without valid execution ID
        document.getElementById("status").textContent = api.i18n.getMessage("resInvalidId") || "Invalid execution ID.";
        return;
    }

    // Build temporary storage key and retrieve data saved by background.js
    const mKey = `exec_${execId}`;
    const data = await api.storage.local.get(mKey);
    const execData = data[mKey];

    if (!execData) {
        // Data could have been deleted if page was reloaded or a previous error occurred
        document.getElementById("status").innerHTML = `<span style="color:red">${api.i18n.getMessage("resDataNotFound") || "Article data not found."}</span>`;
        return;
    }

    // Destructure all fields saved by background.js
    const { markdown, title, service, url, domain, lang, syslang } = execData;

    // Show LLM service name in UI so user knows what's processing
    document.getElementById("serviceName").textContent = service.name;

    try {
        // ---- PROMPT VARIABLE SUBSTITUTION ----
        // Replace each template variable with its real value using global regex (/g)
        // to substitute ALL occurrences in case a variable appears multiple times.
        let pText = service.prompt || "";
        pText = pText.replace(/\{DATE\}/g,    new Date().toLocaleDateString());  // Local date
        pText = pText.replace(/\{HOUR\}/g,    new Date().toLocaleTimeString());  // Local time
        pText = pText.replace(/\{URL\}/g,     url || "");                        // Page URL
        pText = pText.replace(/\{DOMAIN\}/g,  domain || "");                     // Page domain
        pText = pText.replace(/\{LANG\}/g,    lang || "NONE");                   // Document language
        pText = pText.replace(/\{SYSLANG\}/g, syslang || navigator.language);    // System language

        // Build final prompt: user instructions + page content
        // separated by double newline (Markdown convention for separating blocks).
        const promptParams = pText + "\n\n" + markdown;

        const statusDiv = document.getElementById("status");
        const resultBox = document.getElementById("resultBox");
        const saveBtn   = document.getElementById("saveBtn");

        // ---- LIGHTWEIGHT MARKDOWN RENDERER ----
        /**
         * Converts basic Markdown syntax to HTML with inline styles.
         *
         * We don't use an external library (like marked.js) to keep the extension
         * lightweight. We cover most common cases LLMs usually produce:
         *   - Code blocks with and without language specification
         *   - Inline code
         *   - H1, H2, H3 headers
         *   - Bold (**text**) and Italics (*text*)
         *
         * IMPORTANT: First escape special HTML characters (&, <, >)
         * to prevent LLM content HTML from being interpreted by browser
         * (XSS prevention). Then apply Markdown transformations.
         *
         * @param {string} text - Markdown formatted text.
         * @returns {string} Rendered HTML with inline styles.
         */
        function parseMarkdown(text) {
            // 1. Escape HTML characters to prevent XSS and accidental rendering
            let html = text
                .replace(/&/g, "&amp;")
                .replace(/</g, "&lt;")
                .replace(/>/g, "&gt;");

            // 2. Code blocks with language indicator: ```python\ncode\n```
            html = html.replace(
                /```(\w*)\n([\s\S]*?)```/g,
                '<pre style="background:#1e1e2f;padding:10px;border-radius:5px;overflow-x:auto;"><code>$2</code></pre>'
            );

            // 3. Code blocks without language indicator: ```\ncode\n```
            html = html.replace(
                /```([\s\S]*?)```/g,
                '<pre style="background:#1e1e2f;padding:10px;border-radius:5px;overflow-x:auto;"><code>$1</code></pre>'
            );

            // 4. Inline code: `code`
            html = html.replace(
                /`([^`]+)`/g,
                '<code style="background:#444;padding:2px 5px;border-radius:3px;">$1</code>'
            );

            // 5. H3 Header: ### Title
            html = html.replace(
                /^### (.*$)/gim,
                '<h3 style="margin-top:10px; padding-bottom:5px; border-bottom:1px dotted #444;">$1</h3>'
            );

            // 6. H2 Header: ## Title
            html = html.replace(
                /^## (.*$)/gim,
                '<h2 style="margin-top:10px; padding-bottom:5px; border-bottom:1px solid #444;">$1</h2>'
            );

            // 7. H1 Header: # Title
            html = html.replace(
                /^# (.*$)/gim,
                '<h1 style="margin-top:10px; padding-bottom:5px; border-bottom:2px solid #444;">$1</h1>'
            );

            // 8. Bold: **text**
            html = html.replace(/\*\*([^*]+)\*\*/g, '<strong>$1</strong>');

            // 9. Italics: *text*
            html = html.replace(/\*([^*]+)\*/g, '<em>$1</em>');

            return html;
        }

        // ---- REGENERATION SYSTEM WITH RETRIES ----
        let retryCount = 0;       // Current attempt counter
        const MAX_RETRIES = 3;    // Maximum retries before permanent error display

        // IMPORTANT: resultText is declared OUTSIDE startGeneration() so that
        // retries preserve text already received before connection failure.
        // If declared inside, each retry would lose accumulated progress and the
        // model would have to start from zero, potentially entering timeout loop.
        let resultText = "";

        /**
         * Starts (or restarts) connection with background to get LLM streaming.
         *
         * Every startGeneration() call opens a new connection port with background.
         * If connection fails or disconnects unexpectedly, triggerRetry() calls it again
         * until MAX_RETRIES is exceeded.
         */
        function startGeneration() {
            let isDone = false;         // Flag: true when generation completed or failed permanently
            let attemptTimeout = null;  // Active timeout handle to allow cancellation

            // If it's a retry, show retry indicator to user.
            // Keep already received text visible (resultText persists across attempts).
            if (retryCount > 0) {
                statusDiv.style.display = "block";
                const msgBase = api.i18n.getMessage("resReconnecting") || "Retrying query... (Attempt $1/$2)";
                statusDiv.innerHTML = `<span>${msgBase.replace("$1", retryCount).replace("$2", MAX_RETRIES)}</span>`;
                // Don't clear resultBox or resultText: keep previous progress
            }

            // Open communication port with background script.
            // "llm-generate" is the channel name; background.js listens for this name.
            const port = api.runtime.connect({ name: "llm-generate" });

            /**
             * Resets timeout timer.
             *
             * Called:
             * - At start: with isFirstChunk=true (5 min wait for first token).
             * - After each chunk: with isFirstChunk=false (30 s between consecutive tokens).
             *
             * @param {boolean} isFirstChunk - If true, use long timeout (300s), else use short (30s).
             */
            function resetTimeout(isFirstChunk) {
                if (attemptTimeout) clearTimeout(attemptTimeout); // Cancel previous timer
                const delay = isFirstChunk ? 300000 : 30000;      // 5 min vs 30 s
                attemptTimeout = setTimeout(() => {
                    console.warn(`Connection timeout (attempt ${retryCount})`);
                    triggerRetry();
                }, delay);
            }

            /**
             * Handles connection failure (disconnection or timeout) and starts retry.
             * Idempotent thanks to `isDone` flag: only acts first time it's called.
             *
             * @param {boolean} [recoverable=true] - If false, don't retry (e.g.: HTTP 4xx error).
             */
            function triggerRetry(recoverable = true) {
                if (isDone) return; // Avoid duplicate retries if another was already triggered
                isDone = true;
                if (attemptTimeout) clearTimeout(attemptTimeout);
                port.disconnect(); // Close current port before opening new one

                if (recoverable && retryCount < MAX_RETRIES) {
                    retryCount++;
                    startGeneration(); // Retry with new port
                } else {
                    // No more retries: either exceeded MAX_RETRIES or error not recoverable
                    statusDiv.innerHTML = `<span style="color:#ff5555">${api.i18n.getMessage("resConnError") || "Error: Connection closed unexpectedly."}</span>`;
                    statusDiv.style.display = "block";
                    statusDiv.className = ""; // Remove loading animation classes
                }
            }

            // If port disconnects without us asking (e.g.: background crashed),
            // treat disconnection as failure and retry.
            port.onDisconnect.addListener(() => {
                if (!isDone) {
                    console.warn(`Port disconnected unexpectedly (attempt ${retryCount})`);
                    triggerRetry();
                }
            });

            // Send generation request to background with all necessary parameters.
            // background.js will create driver async generator and start sending chunks.
            port.postMessage({
                action:      "generate",
                serviceType: service.type || "ollama",  // Driver type to use
                url:         service.url,               // LLM server URL
                model:       service.model,             // Model name
                apikey:      service.apikey,            // Credentials (can be empty)
                prompt:      promptParams               // Full prompt (instructions + content)
            });

            // Start first chunk wait timeout (5 minutes)
            resetTimeout(true);

            // ---- STREAM MESSAGE LISTENER ----
            port.onMessage.addListener(async (msg) => {
                if (isDone) return; // Ignore messages after finishing or failing

                if (msg.type === "chunk") {
                    // CHUNK RECEIVED: new text fragment from model
                    resetTimeout(false);          // Reset timeout between chunks (30s)
                    statusDiv.style.display = "none";   // Hide loading indicator
                    resultBox.style.display  = "block";   // Show result area

                    if (msg.chunk) {
                        resultText += msg.chunk;                     // Accumulate text
                        resultBox.innerHTML = parseMarkdown(resultText); // Re-render all
                        window.scrollTo(0, document.body.scrollHeight);  // Auto-scroll to bottom
                    }

                } else if (msg.type === "error") {
                    // LLM ERROR: server returned error or exception in background.
                    // HTTP 4xx errors (auth, model not found, etc.) are not
                    // recoverable with retries; only retry on network or 5xx errors.
                    console.error("AI Error:", msg.error);
                    const isRecoverable = !msg.error || !/HTTP 4\d\d/.test(msg.error);
                    triggerRetry(isRecoverable); // Retry only if error is recoverable

                } else if (msg.type === "done") {
                    // GENERATION COMPLETED: stream finished successfully
                    isDone = true;
                    if (attemptTimeout) clearTimeout(attemptTimeout); // Clear timeout

                    // Show "Save" button so user can download result
                    saveBtn.style.display = "inline-block";

                    // Download button handler: generates .md with complete text
                    saveBtn.onclick = () => {
                        const blob = new Blob([resultText], { type: "text/markdown" });
                        const url = URL.createObjectURL(blob);

                        // Generate filename from original page title
                        let cleanTitle = title.replace(/[^a-z0-9]/gi, '_').toLowerCase();
                        if (!cleanTitle || cleanTitle.trim() === "") {
                            cleanTitle = api.i18n.getMessage("resDefaultFilename") || "ai_response";
                        }
                        const filename = cleanTitle + "_ia.md"; // "_ia" suffix to distinguish from raw Markdown

                        api.downloads.download({
                            url: url,
                            filename: filename,
                            saveAs: true // OS "Save as" dialog
                        })
                        .then(() => setTimeout(() => URL.revokeObjectURL(url), 10000))
                        .catch(err => {
                            console.error("Download error:", err);
                            URL.revokeObjectURL(url);
                        });
                    };

                    // Remove storage data to free space.
                    // At this point we no longer need it (UI has text in `resultText`).
                    await api.storage.local.remove(mKey);
                }
            });
        }

        // Start first (and possibly only) generation session
        startGeneration();

    } catch (e) {
        // Capture unexpected errors (e.g.: corrupt storage, malformed data)
        let msg = api.i18n.getMessage("resAIError") || "Error communicating with AI: $1";
        document.getElementById("status").innerHTML = `<span style="color:#ff5555">${msg.replace("$1", e.message)}</span>`;
        document.getElementById("status").className = ""; // Remove loading animation classes
    }
});
