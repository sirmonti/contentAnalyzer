/**
 * @file result.js (Chrome version)
 * @description Result page for the Chrome extension.
 *
 * This page opens automatically in a new tab when the user
 * selects a snippet from a web page and sends it to an LLM service.
 *
 * DATA FLOW:
 * ---------------
 * The content to be processed is not passed directly as URL parameters (it could be
 * too long) but through `storage.local`:
 *
 *   1. background.js saves the data under the key `exec_<id>` in storage.local.
 *   2. background.js opens this page with the URL: `result.html?id=<id>`.
 *   3. This script reads the `id` from the query string, retrieves the data from storage.local
 *      and deletes it when finished (to avoid accumulating obsolete data).
 *
 * BACKGROUND COMMUNICATION ARCHITECTURE:
 * -------------------------------------------------
 * To receive the text generated by the LLM in streaming, an extension Port
 * (runtime.connect) is used, not simple messages. This allows receiving multiple messages
 * during a single session without repeatedly opening and closing the channel.
 *
 * DIFFERENCE WITH FIREFOX VERSION IN TIMEOUTS:
 * ------------------------------------------------
 * In the Firefox version, two different timeouts are used:
 *   - 300,000 ms (5 min) for the first chunk (the model can take a while to start).
 *   - 30,000 ms (30 s) between consecutive chunks.
 *
 * In the Chrome version, both timeouts are 120,000 ms (2 min) because the Chrome
 * service worker has stricter lifespan limitations. A timeout that is
 * too long might not trigger correctly if the service worker is suspended.
 *
 * RETRY SYSTEM:
 * ----------------------
 * The retry system (`MAX_RETRIES = 3`) launches new requests automatically
 * if a timeout or unexpected port disconnection is detected.
 *
 * VARIABLE SUBSTITUTION IN THE PROMPT:
 * ----------------------------------------
 * The prompt configured by the user can contain context variables:
 *   {DATE}    → Current date (toLocaleDateString)
 *   {HOUR}    → Current time (toLocaleTimeString)
 *   {URL}     → Full URL of the source page
 *   {DOMAIN}  → Domain only of the source page
 *   {LANG}    → HTML document language (<html lang="...">)
 *   {SYSLANG} → User's system/browser language (navigator.language)
 */

// Use `window.browser` (Firefox) with fallback to `window.chrome` to maintain
// cross-compatibility in this file which might be shared between versions.
const api = window.browser || window.chrome;

document.addEventListener("DOMContentLoaded", async () => {

    // ---- DATA RETRIEVAL FROM STORAGE ----
    // Read execution ID from URL query string.
    // Example: result.html?id=1712345678901
    const urlParams = new URLSearchParams(window.location.search);
    const execId = urlParams.get('id');

    if (!execId) {
        // Page was opened without a valid ID (direct access or error)
        document.getElementById("status").textContent = api.i18n.getMessage("resInvalidId") || "Invalid execution ID.";
        return;
    }

    // Build storage key and retrieve data saved by background.js
    const mKey = `exec_${execId}`;
    const data = await api.storage.local.get(mKey);
    const execData = data[mKey];

    if (!execData) {
        // Data doesn't exist: could have been deleted or never saved
        document.getElementById("status").innerHTML = `<span style="color:red">${api.i18n.getMessage("resDataNotFound") || "Article data not found."}</span>`;
        return;
    }

    // Destructure all fields saved by background.js
    const { markdown, title, service, url, domain, lang, syslang } = execData;

    // Show name of active LLM service in page header
    document.getElementById("serviceName").textContent = service.name;

    try {
        // ---- VARIABLE SUBSTITUTION IN THE PROMPT ----
        // Replace each template variable with its real value using global regex (`/g`)
        // to substitute ALL occurrences in case the variable appears multiple times.
        let pText = service.prompt || "";
        pText = pText.replace(/\{DATE\}/g,    new Date().toLocaleDateString());   // Current local date
        pText = pText.replace(/\{HOUR\}/g,    new Date().toLocaleTimeString());   // Current local time
        pText = pText.replace(/\{URL\}/g,     url || "");                         // Page URL
        pText = pText.replace(/\{DOMAIN\}/g,  domain || "");                      // Page domain
        pText = pText.replace(/\{LANG\}/g,    lang || "NONE");                    // Document language
        pText = pText.replace(/\{SYSLANG\}/g, syslang || navigator.language);     // System language

        // Final prompt: user instructions followed by page content.
        // Double line break visually separates instructions from content (Markdown style).
        const promptParams = pText + "\n\n" + markdown;

        const statusDiv = document.getElementById("status");
        const resultBox = document.getElementById("resultBox");
        const saveBtn   = document.getElementById("saveBtn");

        // ---- LIGHTWEIGHT MARKDOWN RENDERER ----
        /**
         * Converts basic Markdown syntax to HTML with inline styles.
         *
         * We don't use an external library to keep the extension lightweight.
         * We cover the most common cases LLMs generate:
         *   - Code blocks (with and without language specification)
         *   - Inline code
         *   - H1, H2, H3 headers
         *   - Bold (**text**) and italic (*text*)
         *
         * IMPORTANT: First escape HTML to prevent XSS. Characters &, <, >
         * from LLM text are converted to HTML entities before applying
         * Markdown transformations, preventing the browser from interpreting arbitrary HTML.
         *
         * @param {string} text - text in Markdown format.
         * @returns {string} HTML with inline styles.
         */
        function parseMarkdown(text) {
            // 1. Escape special HTML characters (XSS prevention)
            let html = text
                .replace(/&/g, "&amp;")
                .replace(/</g, "&lt;")
                .replace(/>/g, "&gt;");

            // 2. Code blocks with language indicator: ```python\ncode\n```
            html = html.replace(
                /```(\w*)\n([\s\S]*?)```/g,
                '<pre style="background:#1e1e2f;padding:10px;border-radius:5px;overflow-x:auto;"><code>$2</code></pre>'
            );

            // 3. Code blocks without language indicator: ```\ncode\n```
            html = html.replace(
                /```([\s\S]*?)```/g,
                '<pre style="background:#1e1e2f;padding:10px;border-radius:5px;overflow-x:auto;"><code>$1</code></pre>'
            );

            // 4. Inline code: `code`
            html = html.replace(
                /`([^`]+)`/g,
                '<code style="background:#444;padding:2px 5px;border-radius:3px;">$1</code>'
            );

            // 5–7. H3, H2 and H1 headers (descending order to not process ### as ##)
            html = html.replace(/^### (.*$)/gim, '<h3 style="margin-top:10px; padding-bottom:5px; border-bottom:1px dotted #444;">$1</h3>');
            html = html.replace(/^## (.*$)/gim,  '<h2 style="margin-top:10px; padding-bottom:5px; border-bottom:1px solid #444;">$1</h2>');
            html = html.replace(/^# (.*$)/gim,   '<h1 style="margin-top:10px; padding-bottom:5px; border-bottom:2px solid #444;">$1</h1>');

            // 8. Bold: **text**
            html = html.replace(/\*\*([^*]+)\*\*/g, '<strong>$1</strong>');

            // 9. Italic: *text*
            html = html.replace(/\*([^*]+)\*/g, '<em>$1</em>');

            return html;
        }

        // ---- GENERATION SYSTEM WITH RETRIES ----
        let retryCount = 0;       // Number of retries performed so far
        const MAX_RETRIES = 3;    // Max retries before showing definitive error

        // IMPORTANT: resultText is declared OUTSIDE startGeneration() so that
        // retries preserve the text already received before the connection failure.
        // If declared inside, each retry would lose accumulated progress and the
        // model would have to start from scratch, potentially entering a timeout loop.
        let resultText = "";

        /**
         * Starts (or restarts) the connection with the background to get LLM streaming.
         *
         * Opens a new communication port and sends the generation request.
         * If the connection fails (timeout or disconnection), `triggerRetry()` calls
         * this function again until MAX_RETRIES is reached.
         */
        function startGeneration() {
            let isDone = false;         // true when generation complete or failed definitively
            let attemptTimeout = null;  // Handle for active setTimeout to cancel it

            // If it's a retry, inform the user visually.
            // Keep already received text visible (resultText persists between attempts).
            if (retryCount > 0) {
                statusDiv.style.display = "block";
                const msgBase = api.i18n.getMessage("resReconnecting") || "Retrying query... (Attempt $1/$2)";
                statusDiv.innerHTML = `<span>${msgBase.replace("$1", retryCount).replace("$2", MAX_RETRIES)}</span>`;
                // Don't clear resultBox or resultText: keep previous progress
            }

            // Open communication port with background.
            // "llm-generate" is the channel name; background.js listens for it with onConnect.
            const port = api.runtime.connect({ name: "llm-generate" });

            /**
             * Resets the inactivity timer.
             *
             * NOTE CHROME vs FIREFOX:
             * In Chrome both timeouts are 120s (2 min) because the service worker
             * has shorter lifespan restrictions than Firefox's persistent background
             * script (where 300s and 30s are used respectively).
             *
             * @param {boolean} isFirstChunk - Not used in Chrome; both delays are equal (120s).
             */
            function resetTimeout(isFirstChunk) {
                if (attemptTimeout) clearTimeout(attemptTimeout);
                // In Chrome, we use 120s for both cases (first chunk and between chunks)
                // to adapt to service worker limitations.
                const delay = isFirstChunk ? 120000 : 120000; // 2 minutes in both cases
                attemptTimeout = setTimeout(() => {
                    console.warn(`Hanging connection timeout (attempt ${retryCount})`);
                    triggerRetry();
                }, delay);
            }

            /**
             * Handles a connection failure and initiates a retry.
             * It is idempotent: only acts the first time (thanks to `isDone` flag).
             *
             * @param {boolean} [recoverable=true] - If false, don't retry (e.g.: HTTP 4xx error).
             */
            function triggerRetry(recoverable = true) {
                if (isDone) return; // Avoid duplicate retries
                isDone = true;
                if (attemptTimeout) clearTimeout(attemptTimeout);
                port.disconnect(); // Close port before opening a new one

                if (recoverable && retryCount < MAX_RETRIES) {
                    retryCount++;
                    startGeneration(); // New attempt with a new port
                } else {
                    // No more retries: either by exceeding MAX_RETRIES or non-recoverable error
                    statusDiv.innerHTML = `<span style="color:#ff5555">${api.i18n.getMessage("resConnError") || "Error: Connection closed unexpectedly."}</span>`;
                    statusDiv.style.display = "block";
                    statusDiv.className = ""; // Remove loading animation classes
                }
            }

            // If port disconnects without us asking (e.g.: service worker
            // suspended by Chrome), treat disconnection as a failure and retry.
            port.onDisconnect.addListener(() => {
                if (!isDone) {
                    console.warn(`Port disconnected unexpectedly (attempt ${retryCount})`);
                    triggerRetry();
                }
            });

            // Send generation request to background with all parameters.
            // background.js will select the correct driver and start streaming.
            port.postMessage({
                action:      "generate",
                serviceType: service.type || "ollama",  // Driver type to use
                url:         service.url,               // LLM server base URL
                model:       service.model,             // Model name to use
                apikey:      service.apikey,            // Credentials (can be empty)
                prompt:      promptParams               // Full prompt: instructions + content
            });

            // Start waiting timeout (2 min for first chunk in Chrome)
            resetTimeout(true);

            // ---- STREAM MESSAGE LISTENER ----
            port.onMessage.addListener(async (msg) => {
                if (isDone) return; // Ignore messages if already finished or failed

                if (msg.type === "chunk") {
                    // CHUNK RECEIVED: new text snippet generated by the model
                    resetTimeout(false);                      // Reset timeout between chunks
                    statusDiv.style.display = "none";         // Hide "Processing..." loader
                    resultBox.style.display = "block";        // Show result area

                    if (msg.chunk) {
                        resultText += msg.chunk;                      // Accumulate text
                        resultBox.innerHTML = parseMarkdown(resultText); // Re-render everything
                        window.scrollTo(0, document.body.scrollHeight);  // Auto scroll to bottom
                    }

                } else if (msg.type === "error") {
                    // LLM ERROR: exception in background or server responded with error.
                    // HTTP 4xx errors (authentication, model not found, etc.) are not
                    // recoverable with retries; we only retry for network or 5xx errors.
                    console.error("AI Error:", msg.error);
                    const isRecoverable = !msg.error || !/HTTP 4\d\d/.test(msg.error);
                    triggerRetry(isRecoverable); // Retry only if error is recoverable

                } else if (msg.type === "done") {
                    // GENERATION COMPLETED: stream finished correctly
                    isDone = true;
                    if (attemptTimeout) clearTimeout(attemptTimeout);

                    // Show "Save" button so user can download the result
                    saveBtn.style.display = "inline-block";

                    // Download button handler
                    saveBtn.onclick = () => {
                        const blob = new Blob([resultText], { type: "text/markdown" });
                        const url = URL.createObjectURL(blob); // Works in extension pages
                        
                        let cleanTitle = title.replace(/[^a-z0-9]/gi, '_').toLowerCase();
                        if (!cleanTitle || cleanTitle.trim() === "") {
                            cleanTitle = api.i18n.getMessage("resDefaultFilename") || "ai_response";
                        }
                        const filename = cleanTitle + "_ia.md"; // "_ia" suffix to distinguish from raw Markdown

                        api.downloads.download({
                            url: url,
                            filename: filename,
                            saveAs: true // OS "Save as" dialog
                        })
                        .then(() => setTimeout(() => URL.revokeObjectURL(url), 10000))
                        .catch(err => {
                            console.error("Download error:", err);
                            URL.revokeObjectURL(url);
                        });
                    };

                    // Remove storage data to free space.
                    // At this point text is already in `resultText`; we don't need storage.
                    await api.storage.local.remove(mKey);
                }
            });
        }

        // Start first (and possibly only) generation session
        startGeneration();

    } catch (e) {
        // Catch unexpected errors (e.g.: corrupt storage, malformed data)
        let msg = api.i18n.getMessage("resAIError") || "Error communicating with AI: $1";
        document.getElementById("status").innerHTML = `<span style="color:#ff5555">${msg.replace("$1", e.message)}</span>`;
        document.getElementById("status").className = ""; // Remove loading animation
    }
});
